# Operadores matematicos em Ruby

num1 = 12
num2 = 10

# Soma
puts num1 + num2

# Subtração
puts num1 - num2

# Multiplicação
puts num1 * num2

# Divisão (Retorna o Quociente ou número real da divisão)
puts num1 / num2

# Módulo (Retorna o resto da divisão, realizando a operação até o sobrar o resto)
puts num1 % num2

# Exponenciação
puts num1 ** num2